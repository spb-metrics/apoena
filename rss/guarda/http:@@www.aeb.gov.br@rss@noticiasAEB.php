<description>
   Os visitantes da 62ª Reunião Anual da Sociedade Brasileira para o Progresso da Ciência (SBPC) conheceram, por meio de exposições, minicursos e vídeos, um pouco mais do Programa Espacial Brasileiro. Cerca de cinco mil pessoas passaram diariamente pela SBPC que, este ano, teve como tema Ciências do mar: herança para o futuro, e foi realizada, entre os dias 25 e 30 de junho, na Universidade Federal do Rio Grande do Norte, em Natal (RN).  
 
