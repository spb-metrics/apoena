<title>
   Missão Público-Privada do Japão na Área Espacial visita AEB 
</title>
<description>
         Representantes do governo e de empresas japonesas na área espacial estiveram, na tarde dessa terça-feira, 24 de agosto, na Agência Espacial Brasileira (AEB). A visita é parte da  Missão Público-Privada do Japão na Área Espacial que pretende aprofundar o conhecimento acerca do programa espacial dos dois países e  sondar possibilidades concretas de cooperação no setor.  
 
 Segundo o o presidente da missão, Tomori Nishimura, o país criou, há dois anos, uma política de espaço e cooperação internacional. Acredito que encontraremos uma área de interesse entre os dois países. Esperamos que as tecnologias espacias do Japão possam contribuir com o progresso e desenvolvimento do Brasil.  
 
 Após assistir apresentações de empresas japonesas como a  Mitsubishi Heavy Motors ,  Pasco Corporation   e  AxelSpace , o presidente da AEB, Carlos Ganem, disse que há um conjunto de informações e oportunidades trazidas pelas apresentações que coincidem com o que a AEB deseja realizar em 2010. Acredito que surgirão possibilidades de cooperação com o Japão.   
 
  Inpe - Os japoneses ficam no Brasil até dia 28 de agosto.  Nos dias 26 e 27, os temas ligados às políticas e programas da indústria espacial do Japão e do Brasil serão discutidos durante workshop no Instituto Nacional de Pesquisas Espaciais (Inpe), em São José dos Campos (SP).  
 
 O evento é promovido em parceria pelo Inpe, Ministério da Economia, Comércio e Indústria do Japão e Sociedade de Companhias Aeroespaciais do Japão (Sjac, na sigla em inglês).  
 
 O workshop será aberto por João Braga, vice-diretor e coordenador dos Centros Regionais do Inpe. Em seguida, haverá uma palestra de Himilcon de Castro Carvalho, diretor de Política Espacial e Investimentos Estratégicos da Agência Espacial Brasileira (AEB).  
 
 Durante os dois dias de workshop especialistas brasileiros e japoneses de instituições governamentais e privadas apresentarão alguns de seus principais projetos ligados à área espacial.  
 
 A comitiva japonesa visita, ainda, os laboratórios do Departamento de Ciência e Tecnologia Aeroespacial (DCTA) do Ministério da Defesa (SP). Depois do Brasil eles seguem para a Argentina e para o Peru.              
